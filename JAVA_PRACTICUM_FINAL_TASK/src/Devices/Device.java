package Devices;

import java.util.Comparator;

public abstract class Device {
    private String brand ;
    private int price;
    public Device(String brand, int price){
        this.brand = brand;
        this.price = price;

    }
    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }



}//end of class
